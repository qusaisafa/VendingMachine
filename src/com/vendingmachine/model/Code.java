package com.vendingmachine.model;

/*
 *  defines items code depending on there location on machine 
 *  each Row defined with two characters
 *  example : A1 means item on first row and first column(rowcolumn)
 */
public enum Code {
	 A1, A2, A3, A4, A5, // row #1
	 
	 B1, B2, B3, B4, B5, // row #2
	 
	 C1, C2, C3, C4, C5, // row #3
	 
	 D1, D2, D3, D4, D5, // row #4
	
	 E1, E2, E3, E4, E5; // row #5
	
	
}
